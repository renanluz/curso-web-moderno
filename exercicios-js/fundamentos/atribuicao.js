const a = 7

let b = 3

// Atribuição aditiva: b = b + a ou b = 7 +3
b += a 
console.log(b)

// Atribuição subtrativa: b = b - 4 ou b = 10 - 4
b -= 4
console.log(b)

// Atribuição multiplicativa: b = b * 2 ou b = 6 * 2
b *= 2 
console.log(b)

// Atribuição divisiva: b = b / 2 ou b = 12 / 2
b /= 2
console.log(b)

// Atribuição modular: b = b % 2 ou b = 6 % 2
b %= 2
console.log(b)
